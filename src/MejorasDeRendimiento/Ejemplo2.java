package MejorasDeRendimiento;

//Mejora en la libreria Math

public class Ejemplo2 {

    public static void main(String[] args) {

        double resultado = Math.pow(10, 2); //Optimización del calculo de potencia

        double raiz = Math.sqrt(16); //Optimización de raiz cuadrada

        System.out.println("El resultado de 10^2: " + resultado);
        System.out.println("El raiz cuadrada de 16: " + raiz);

    }
}
